
# Pasar de texto a morce
alfabeto_morse = {
    "a": ".-", "b": "-...", "c": "-.-.", "d": "-..", "e": ".",
    "f": "..-.", "g": "--.", "h": "....", "i": "..", "j": ".---",
    "k": "-.-", "l": ".-..", "m": "--", "n": "-.", "o": "---",
    "p": ".--.", "q": "--.-", "r": ".-.", "s": "...", "t": "-",
    "u": "..-", "v": "...-", "w": ".--", "x": "-..-", "y": "-.--",
    "z": "--..", " ": " "
}

def traduccion(texto):
    texto = list(texto.lower())
    return " ".join([alfabeto_morse[i] for i in texto if i in alfabeto_morse.keys()])